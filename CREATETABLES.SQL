--COURSES (CRS_ID,CRS_NAME,CRS_HOURS,CRS_PRICE,RND_DAYS)
CREATE TABLE COURSES
(CRS_ID NUMBER(4) CONSTRAINT CRS_ID_PK PRIMARY KEY,
CRS_NAME VARCHAR2(100),
CRS_HOURS NUMBER(4),
CRS_PRICE NUMBER(8,2),
RND_DAYS VARCHAR2(100)
);

--STUDENTS(ST_ID,ST_NAME,ST_MOBILE,ST_ADDRESS)
CREATE TABLE STUDENTS
(ST_ID NUMBER(4) CONSTRAINT ST_ID_PK PRIMARY KEY,
ST_NAME VARCHAR2(100),
ST_MOBILE VARCHAR2(100),
ST_ADDRESS VARCHAR2(100)
);
--ROUNDS(RND_ID,RND_NAME,RND_START_DATE,RND_END_DATE,RND_PRICE,CRS_ID)
CREATE TABLE ROUNDS
(RND_ID NUMBER(4) CONSTRAINT RND_ID_PK PRIMARY KEY,
RND_NAME VARCHAR2(100),
RND_START_DATE DATE,
RND_END_DATE DATE,
RND_PRICE NUMBER(8,2),
CRS_ID NUMBER(4) REFERENCES COURSES(CRS_ID) --LINK THIS COLUMN AS A FOREIGN KEY REFERE TO COURSES TABLE
);



--STUDENTS_ROUNDS(ID,ST_ID,RND_ID,PAID_AMOUNT)
CREATE TABLE STUDENTS_ROUNDS
(ID NUMBER(4) CONSTRAINT ST_RND_ID_PK PRIMARY KEY,
ST_ID NUMBER(4) REFERENCES STUDENTS(ST_ID) ,--LINK THIS COLUMN AS A FOREIGN KEY REFERE TO STUDENTS TABLE
RND_ID NUMBER(4) REFERENCES ROUNDS(RND_ID),--LINK THIS COLUMN AS A FOREIGN KEY REFERE TO ROUNDS TABLE
PAID_AMOUNT NUMBER(8,2)

);

