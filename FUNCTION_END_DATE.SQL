CREATE OR REPLACE  FUNCTION CALC_END_DATE(V_NAME_DAY1 VARCHAR2,V_NAME_DA2 VARCHAR2,V_NUMBER_ATTEND NUMBER)
RETURN DATE
IS
V_END_DATE DATE :=SYSDATE;
V_COUNT NUMBER :=0;
V_CHECK NUMBER :=V_NUMBER_ATTEND -1; --CHECK FOR ODD NUMBER OF TIMES OF ATTENDENCY
BEGIN 
          WHILE V_COUNT<V_NUMBER_ATTEND  LOOP --V_NUMBER_ATTEND REFER TO NUMBER OF HOURS MULTIPLY BY 4 TO GET NUMBER OF TIMES THIS COURSES NEED
              V_END_DATE:=NEXT_DAY(V_END_DATE,V_NAME_DAY1 ); --FIRST PART OF COLUMN RND_DAYS IN COURSES TABLE
               V_END_DATE:=NEXT_DAY(V_END_DATE,V_NAME_DA2 ); --SECOND PART OF COLUMN RND_DAYS IN COURSES TABLE
               V_COUNT:=V_COUNT+2;
               IF V_COUNT=V_CHECK THEN --FOR ODD NUMBER BELONG TO TIMES OF COURSES ATTENDENCY
                    V_END_DATE:=NEXT_DAY(V_END_DATE,V_NAME_DAY1);
                    V_COUNT:=V_COUNT+2;
               END IF;
          END LOOP;
RETURN V_END_DATE;
END;