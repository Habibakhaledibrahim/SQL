--THIS FUNCTION 'S WILL BE USED IN TWO FUNCTIONS OF CALCULATING START AND END DATE
CREATE OR REPLACE  TYPE DAY_OB AS OBJECT(V_NAME_DAY1 VARCHAR2(50),V_NAME_DAY2 VARCHAR2(50));
CREATE OR REPLACE  TYPE DAY_TA IS TABLE OF DAY_OB;
CREATE OR REPLACE FUNCTION GET_DAY_NAME(V_DAYS VARCHAR2)
RETURN DAY_TA
IS
T_DAY_TAB  DAY_TA:=DAY_TA();
V_DAY1 VARCHAR2(50);
V_DAY2 VARCHAR2(50);
C  INTEGER :=1;
BEGIN
T_DAY_TAB.EXTEND;
V_DAY1:=SUBSTR(V_DAYS,1,INSTR(V_DAYS,',')-1);
V_DAY2:=SUBSTR(V_DAYS,INSTR(V_DAYS,',')+1);
T_DAY_TAB(C):= DAY_OB(V_DAY1,V_DAY2);
RETURN T_DAY_TAB; 
END;

